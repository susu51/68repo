def get_city_coordinates(city_name: str, district_name: str = None):
    """
    Get GPS coordinates for a Turkish city or district
    
    Args:
        city_name: Name of the city (e.g., "İstanbul", "Ankara")
        district_name: Optional district name (e.g., "Kadıköy", "Çankaya")
    
    Returns:
        dict: {"lat": float, "lng": float} or None if not found
    """
    if city_name not in TURKISH_CITIES_COORDS:
        return None
    
    city_data = TURKISH_CITIES_COORDS[city_name]
    
    # If district is specified and exists, return district coordinates
    if district_name and "districts" in city_data:
        districts = city_data["districts"]
        if district_name in districts:
            return districts[district_name]
    
    # Return city center coordinates
    return {"lat": city_data["lat"], "lng": city_data["lng"]}

def get_all_cities():
    """Get list of all Turkish cities"""
    return list(TURKISH_CITIES_COORDS.keys())

def get_city_districts(city_name: str):
    """Get list of districts for a city"""
    if city_name not in TURKISH_CITIES_COORDS:
        return []
    
    city_data = TURKISH_CITIES_COORDS[city_name]
    if "districts" in city_data:
        return list(city_data["districts"].keys())
    
    return ["Merkez"]
